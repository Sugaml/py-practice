#Integer
age=25
quantity=100

print("Age :: ",age ," Quantity :: ",quantity )

#Float
pi=3.14
temperature=-2.5
print("PI  :: ",pi ," Temperature :: ",temperature )

#String (`str`)
name="Sugam Lama"
message="Welcome to Python"
print(name)
print(message)

# Boolean
is_true=True
is_false=False
print(is_true)
print(is_false)

#list
numbers=[1,2,3,14,58,55,42]
colors=["red","green","blue","white","black"]
