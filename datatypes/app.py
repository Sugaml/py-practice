print(colors)


#Tuple
coordinates=(10,20)
colors=("red","green","blue")
print(coordinates)
print(colors)


#Set
unique_numbers={1,2,3,4,5,6,7}
unique_letters={'a','b','c','d','e'}
print(unique_numbers)
print(unique_letters)

